cadena1 = "ceremonia en mi casa bro"
cadena2 = "a david le gustan las minas"
cadena3 = "quererse"

#COUNT

#buscamos una letra  o palabra, devuelve la cantidad de veces que coincida
contar = cadena1.count("a")
contar2 = cadena2.count("a")
contar3 = cadena3.count("a")

print(contar)
print(contar2)
print(contar3)

#LEN

#contamos cuantos caracteres tiene una cadena
#len no es un metodo, es una funcion
#len(cadena)
contar_caracteres = len(cadena1)
contar_caracteres2 = len(cadena2)
contar_caracteres3 = len(cadena3)

print(contar_caracteres)
print(contar_caracteres2)
print(contar_caracteres3)